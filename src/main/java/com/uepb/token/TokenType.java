package com.uepb.token;

public enum TokenType {
    EOF,        // fim de arquivo
    VAR,        // variável
    NUMBER,     // números
    PLUS,       // operador +
    MINUS,      // operador -
    MULTIPLY,   // operador *
    DIVIDE,     // operador /
    LPAREN,     // (
    RPAREN,     // )        
    SEMICOLON,  // ;
    POWER;      // ^

}

